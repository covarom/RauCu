package sample.shopping;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.util.ArrayList;
import java.util.Date;
import java.util.List;
import sample.utils.DBUtils;

/**
 *
 * @author Doan
 */
public class CategoryDAO {
    private static final String SEARCH= "SELECT * from tblCategory";
    private static final String UPDATE = "UPDATE tblCategory SET catagoryName=? WHERE catagoryID=?";
    private static final String DELETE ="DELETE tblCategory WHERE catagoryID=?";
    private static final String CREATE = "INSERT INTO tblCategory(catagoryID, catagoryName) VALUES(?,?)";
    private static final String CHECK_DUPLICATE = "Select catagoryName FROM tblCategory WHERE catagoryID=?";
    
    
     public boolean checkDuplicate(int categoryID) throws SQLException {
        boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        ResultSet rs = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(CHECK_DUPLICATE);
                ptm.setString(1, Integer.toString(categoryID));
                rs = ptm.executeQuery();
                if (rs.next()) {
                    check = true;
                }
            }
        } catch (Exception e) {

        } finally {
            if (rs != null) {
                rs.close();
            }
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
    }
    
    public boolean create(Category cate) throws SQLException{
           boolean check=false;
           Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(CREATE);
                ptm.setString(1, Integer.toString(cate.getCatagoryID()));
                ptm.setString(2, cate.getCatagoryName());
                check = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
       }
    
    public boolean delete(String catagoryID) throws SQLException {
        boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(DELETE);
                ptm.setString(1, catagoryID);
                check = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
    }
    
    public boolean update(Category cate) throws SQLException {
        boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(UPDATE);
                ptm.setString(1, cate.getCatagoryName());
                ptm.setString(2, Integer.toString(cate.getCatagoryID()));
                check = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
    }
    
    public List<Category>getListCate() throws SQLException{
         List<Category> list = new ArrayList<Category>();
        Connection conn = null;
        PreparedStatement ptm = null;
        ResultSet rs = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(SEARCH);              
                rs = ptm.executeQuery();
                while (rs.next()) {
                    int catagoryID = rs.getInt("catagoryID");
                    String catagoryName = rs.getString("catagoryName");                             
                    list.add(new Category(catagoryID,catagoryName));
                }
            }
        } catch (Exception e) {
             e.printStackTrace();
        } finally {
            if (rs != null) {
                rs.close();
            }
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return list;
        
    }
}
